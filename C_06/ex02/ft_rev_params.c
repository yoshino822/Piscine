#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	k;
	int	i;

	k = argc - 1;
	if (argc > 0)
	{
		while (k > 0)
		{
			i = 0;
			while (argv[k][i])
			{
				i++;
			}
			write(1, argv[k], i);
			write(1, "\n", 1);
			k--;
		}
	}
	return (0);
}
